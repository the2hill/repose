package org.openrepose.lint.commands

import org.junit.runner.RunWith
import org.scalatest.junit.JUnitRunner
import org.scalatest.{FunSpec, Matchers}

@RunWith(classOf[JUnitRunner])
class CommandRegistryTest extends FunSpec with Matchers {

  describe("lookup") {
    it("should return Some Command if provided a supported command token") {
      val result = CommandRegistry.lookup(VerifyTryItNowCommand.getCommandToken)

      result shouldBe a[Some[_]]
      result.get shouldBe a[Command]
    }

    it("should return None if provided an unsupported command token") {
      CommandRegistry.lookup("foo") should be theSameInstanceAs None
    }
  }

  describe("getAvailableCommands") {
    it("should return a Set of supported Commands") {
      val result = CommandRegistry.getAvailableCommands

      result shouldBe a[Set[_]]
      result should contain theSameElementsAs Set(VerifyTryItNowCommand)
    }
  }
}
