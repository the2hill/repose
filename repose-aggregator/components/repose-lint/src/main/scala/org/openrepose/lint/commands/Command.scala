package org.openrepose.lint.commands

import org.openrepose.lint.LintConfig

/**
  * Some action or behavior associated with a unique identifier.
  */
trait Command {

  /**
    * @return a unique identifier used to invoke a command
    */
  def getCommandToken: String

  /**
    * @return a description of a command
    */
  def getCommandDescription: String

  /**
    * Performs some action.
    */
  def perform(config: LintConfig): Unit
}
